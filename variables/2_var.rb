puts "How old are you?"

age = gets.chomp.to_i

ten_year = age + 10

twenty_year = age + 20

thirty_year = age + 30

forty_year = age + 40

puts "In ten years, you will be #{ten_year} years old."

puts "In twenty years, you will be #{twenty_year} years old."

puts "In thirty years, you will be #{thirty_year} years old."

puts "In forty years, you will be #{forty_year} years old."